/*
 * Name: Math.log2
 * Syntax: Math.log2(x)
 * Description: Returns an implementation-dependent approximation to the base 2 logarithm of x.
 * Spec: https://www.ecma-international.org/ecma-262/6.0/index.html#sec-math.log2
 *
 */

 // Base 2 logarithm 0
 console.log("The base 2 logarithm of 0 is ",Math.log2(0));

 // base 2 logarithm of -1
 console.log("The base 2 logarithm of -1 is ",Math.log2(-1));

 // base 2 logarithm of 1
 console.log("The base 2 logarithm of 1 is ",Math.log2(1));

 // base 2 logarithm of 9
 console.log("The base 2 logarithm of 9 is ",Math.log2(9));

 // base 2 logarithm of 0.5
 console.log("The base 2 logarithm of 0.5 is ",Math.log2(0.5));

 // base 2 logarithm of 100
 console.log("The base 2 logarithm of 100 is ",Math.log2(100));
